    importFromExcel(file) {
        return new Promise((resolve, reject) => {
            if (!file) { reject(new Error('No file provided')); return; }
            if (!file.name.endsWith('.xlsx')) { reject(new Error('Invalid file type. Please select an .xlsx file.')); return; }
            if (file.size > 10 * 1024 * 1024) { reject(new Error('File size too large. Maximum size is 10MB.')); return; }

            const reader = new FileReader();
            reader.onload = (e) => {
                try {
                    const wb = XLSX.read(e.target.result, { type: 'array', cellDates: false });

                    for (const name of REQUIRED_SHEETS) {
                        if (!wb.SheetNames.includes(name)) throw new Error(`Missing required sheet: "${name}"`);
                    }

                    const cashResult = parseCashRows(getSheetRows(wb, SHEET_NAMES.CASH));
                    const invResult = parseInvestmentsRows(getSheetRows(wb, SHEET_NAMES.INVESTMENTS));
                    const ordResult = parseOrdersRows(getSheetRows(wb, SHEET_NAMES.ORDERS), invResult);
                    const liabilities = parseLiabilitiesRows(getSheetRows(wb, SHEET_NAMES.LIABILITIES));
                    const creditCards = parseCreditCardsRows(getSheetRows(wb, SHEET_NAMES.CREDIT_CARDS));
                    const cfResult = parseCashFlowRows(getSheetRows(wb, SHEET_NAMES.CASHFLOW));
                    const settings = parseSettingsRows(getSheetRows(wb, SHEET_NAMES.SETTINGS));

                    resolve({
                        savings: cashResult.savings,
                        fixedDeposits: cashResult.fixedDeposits,
                        mutualFunds: invResult.mutualFunds,
                        stocks: invResult.stocks,
                        crypto: invResult.crypto,
                        liabilities,
                        creditCards,
                        transactions: cfResult.transactions,
                        budgets: cfResult.budgets,
                        settings,
                        mfOrders: ordResult.mfOrders,
                        stockOrders: ordResult.stockOrders,
                        cryptoOrders: ordResult.cryptoOrders,
                    });
                } catch (err) {
                    reject(new Error('Failed to parse Excel file: ' + err.message));
                }
            };
            reader.onerror = () => reject(new Error('Failed to read file'));
            reader.readAsArrayBuffer(file);
        });
    },
