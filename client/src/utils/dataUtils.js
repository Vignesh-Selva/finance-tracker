function toNum(val) {
    if (val === null || val === undefined || val === '') return null;
    const n = Number(val);
    return isNaN(n) ? null : n;
}

function toBool(val) {
    if (typeof val === 'boolean') return val;
    if (typeof val === 'string') return val.trim().toUpperCase() === 'TRUE';
    return Boolean(val);
}

function toDateStr(val) {
    if (!val) return null;
    if (typeof val === 'number') {
        const date = XLSX.SSF.parse_date_code(val);
        if (!date) return null;
        const m = String(date.m).padStart(2, '0');
        const d = String(date.d).padStart(2, '0');
        return `${date.y}-${m}-${d}`;
    }
    if (typeof val === 'string' && val.trim()) {
        const s = val.trim();
        // Try to parse various date formats
        // YYYY-MM-DD, YYYY/MM/DD
        if (/^\d{4}[-/]\d{1,2}[-/]\d{1,2}$/.test(s)) {
            const parts = s.split(/[-/]/);
            const y = parts[0];
            const m = String(parts[1]).padStart(2, '0');
            const d = String(parts[2]).padStart(2, '0');
            return `${y}-${m}-${d}`;
        }
        // MM/DD/YYYY, DD/MM/YYYY (try both interpretations)
        if (/^\d{1,2}[-/]\d{1,2}[-/]\d{4}$/.test(s)) {
            const parts = s.split(/[-/]/);
            // Assume MM/DD/YYYY format (common in Excel)
            const m = String(parts[0]).padStart(2, '0');
            const d = String(parts[1]).padStart(2, '0');
            const y = parts[2];
            return `${y}-${m}-${d}`;
        }
        return s;
    }
    return null;
}

function strip(item, keepId = false) {
    const { id, portfolio_id: _pid, created_at: _ca, updated_at: _ua, ...rest } = item;
    return keepId ? { id, ...rest } : rest;
}

