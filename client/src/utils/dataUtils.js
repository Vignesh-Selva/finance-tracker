function getSheetRows(wb, sheetName) {
    const ws = wb.Sheets[sheetName];
    if (!ws) throw new Error(`Missing required sheet: "${sheetName}"`);
    return XLSX.utils.sheet_to_json(ws, { defval: '' });
}

function parseCashRows(rows) {
    const savings = [];
    const fixedDeposits = [];
    for (const row of rows) {
        const t = String(row.table_type || '').trim();
        if (t === 'Savings') {
            savings.push({ bank_name: row.bank_name, account_type: row.account_type, balance: toNum(row.balance), interest_rate: toNum(row.interest_rate) });
        } else if (t === 'FixedDeposit') {
            fixedDeposits.push({ bank_name: row.bank_name, invested: toNum(row.invested), maturity: toNum(row.maturity), interest_rate: toNum(row.interest_rate), maturity_date: toDateStr(row.maturity_date) });
        } else if (t) {
            throw new Error(`Cash sheet: unknown table_type "${t}". Expected "Savings" or "FixedDeposit".`);
        }
    }
    return { savings, fixedDeposits };
}

function parseInvestmentsRows(rows) {
    const mutualFunds = [];
    const stocks = [];
    const crypto = [];
    for (const row of rows) {
        const t = String(row.asset_type || '').trim();
        const id = row.id ? String(row.id).trim() : undefined;
        if (t === 'MutualFund') {
            const obj = { fund_name: row.name, scheme_code: row.symbol, units: toNum(row.quantity), invested: toNum(row.invested), current: toNum(row.current), current_price: toNum(row.current_price), fund_type: row.category, sip: toNum(row.sip) };
            if (id) obj.id = id;
            mutualFunds.push(obj);
        } else if (t === 'Stock') {
            const obj = { stock_name: row.name, ticker: row.symbol, quantity: toNum(row.quantity), invested: toNum(row.invested), current: toNum(row.current), current_price: toNum(row.current_price), sector: row.category };
            if (id) obj.id = id;
            stocks.push(obj);
        } else if (t === 'Crypto') {
            const obj = { coin_name: row.name, quantity: toNum(row.quantity), invested: toNum(row.invested), current: toNum(row.current), current_price: toNum(row.current_price), platform: row.category };
            if (id) obj.id = id;
            crypto.push(obj);
        } else if (t) {
            throw new Error(`Investments sheet: unknown asset_type "${t}". Expected "MutualFund", "Stock", or "Crypto".`);
        }
    }
    return { mutualFunds, stocks, crypto };
}

function parseOrdersRows(rows, holdings = { mutualFunds: [], stocks: [], crypto: [] }) {
    const mfOrders = [];
    const stockOrders = [];
    const cryptoOrders = [];
    for (const row of rows) {
        const t = String(row.asset_type || '').trim();
        const name = String(row.name || '').trim();
        const holdingId = String(row.holding_id || '').trim();
        const base = { execution_date: toDateStr(row.execution_date) || row.execution_date, order_type: row.order_type, amount: toNum(row.amount), charges: toNum(row.charges) ?? 0, platform: row.platform || '', remarks: row.remarks || '', amount_overridden: toBool(row.amount_overridden) };

        if (t === 'MutualFund') {
            let mfId = holdingId;
            if (!mfId && name) {
                const matched = holdings.mutualFunds?.find(m => m.fund_name?.toLowerCase() === name.toLowerCase());
                if (matched) mfId = matched.id;
            }
            mfOrders.push({ ...base, mf_id: mfId, units: toNum(row.units), nav: toNum(row.nav) });
        } else if (t === 'Stock') {
            let stockId = holdingId;
            if (!stockId && name) {
                const matched = holdings.stocks?.find(s => s.stock_name?.toLowerCase() === name.toLowerCase());
                if (matched) stockId = matched.id;
            }
            stockOrders.push({ ...base, stock_id: stockId, quantity: toNum(row.quantity), price: toNum(row.price) });
        } else if (t === 'Crypto') {
            let cryptoId = holdingId;
            if (!cryptoId && name) {
                const matched = holdings.crypto?.find(c => c.coin_name?.toLowerCase() === name.toLowerCase());
                if (matched) cryptoId = matched.id;
            }
            cryptoOrders.push({ ...base, crypto_id: cryptoId, quantity: toNum(row.quantity), price: toNum(row.price) });
        } else if (t) {
            throw new Error(`Orders sheet: unknown asset_type "${t}". Expected "MutualFund", "Stock", or "Crypto".`);
        }
    }
    return { mfOrders, stockOrders, cryptoOrders };
}

function parseLiabilitiesRows(rows) {
    return rows.filter(r => String(r.type || '').trim()).map(r => ({ type: r.type, lender: r.lender, loan_amount: toNum(r.loan_amount), outstanding: toNum(r.outstanding), interest_rate: toNum(r.interest_rate), emi: toNum(r.emi) }));
}

function parseCreditCardsRows(rows) {
    return rows.filter(r => String(r.card_name || '').trim()).map(r => ({ card_name: r.card_name, bank_name: r.bank_name, last4: r.last4, credit_limit: toNum(r.credit_limit), current_balance: toNum(r.current_balance), statement_balance: toNum(r.statement_balance), due_date: r.due_date || '', billing_date: r.billing_date || '' }));
}

function parseCashFlowRows(rows) {
    const transactions = [];
    const budgets = [];
    for (const row of rows) {
        const t = String(row.table_type || '').trim();
        if (t === 'Transaction') {
            transactions.push({ date: toDateStr(row.date) || row.date, type: row.type, category: row.category, amount: toNum(row.amount), description: row.description || '' });
        } else if (t === 'Budget') {
            budgets.push({ category: row.category, monthly_limit: toNum(row.monthly_limit), notes: row.notes || '' });
        } else if (t) {
            throw new Error(`CashFlow sheet: unknown table_type "${t}". Expected "Transaction" or "Budget".`);
        }
    }
    return { transactions, budgets };
}

